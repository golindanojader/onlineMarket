function cambia_provincia(){ 
   	//tomo el valor del select del pais elegido 
   	var estate 
   	var idEstate;
   		// estate = document.EstateMun.estate[document.EstateMun.estate.selectedIndex].value 
  		estate = document.EstateMun.estate[document.EstateMun.estate.selectedIndex].id
  		

   	//miro a ver si el pais está definido 
   	if (estate != 0) { 
      	//si estaba definido, entonces coloco las opciones de la provincia correspondiente. 
      	//selecciono el array de provincia adecuado 
      	myMunicipality=allMunicipality[estate]
      	//calculo el numero de provincias 
      	num_provincias = myMunicipality.length 
      	//marco el número de provincias en el select 
      	document.EstateMun.municipality.length = num_provincias 
      	//para cada provincia del array, la introduzco en el select 
      	for(i=0;i<num_provincias;i++){ 
         	document.EstateMun.municipality.options[i].value=myMunicipality[i] 
         	document.EstateMun.municipality.options[i].text=myMunicipality[i] 
      	}	
   	}else{ 
      	//si no había provincia seleccionada, elimino las provincias del select 
      	document.EstateMun.municipality.length = 1 
      	//coloco un guión en la única opción que he dejado 
      	document.EstateMun.municipality.options[0].value = "-" 
      	document.EstateMun.municipality.options[0].text = "-" 
   	} 
   	//marco como seleccionada la opción primera de provincia 
   	document.EstateMun.municipality.options[0].selected = true 
}

